export const format = (userInfo) => {
  let data = {
    basic: {
      kind: 'gaokao',
      user_info: {
        division: division[userInfo.discipline] || 'art',
        location: userInfo.location,
      },
      is_completed: true,
    },
    gaokao: {
      bonus: {
        key: userInfo.awarded.kind,
        score: userInfo.awarded.score,
      },
      score: {
        kind: 'estimated',
        score: userInfo.total_score,
        // "estimated"/"actual": 高考分数,
      },
      preference: preference[userInfo.fill] || 'field_of_study',
      independent_recruitment: [],
      is_completed: true,
    },
    interest: {
      field_of_study_keys: [],
      field_of_study_category_keys: userInfo.professional,
      institute_locations: userInfo.regional,
      is_completed: true,
    },
  };

  userInfo.recruit.forEach((r, i) => {
    if (r.slug) {
      data.gaokao.independent_recruitment.push({
        institute_slug: r.slug,
        score: r.score,
      });
    }
  });

  professionalData.forEach((r, i) => {
    professionalListData[r.key].forEach((item, index) => {
      if (userInfo.professional && userInfo.professional.indexOf(item.key) > -1) {
        data.interest.field_of_study_keys.push(r.key);
        return;
      }
    });
  });

  return data;
};

export const dataToJson = (data) => {
  let userInfo = {
    id: data.basic._id,
    location: data.basic.user_info.location,
    total_score: data.gaokao.score.score,
    discipline: reDivision[data.basic.user_info.division] || '文科',
    recruit: [],
    awarded: {
      kind: data.gaokao.bonus.key,
      score: data.gaokao.bonus.score,
      name: '',
    },
    regional: data.interest.institute_locations,
    regionalName: [],
    professional: data.interest.field_of_study_category_keys,
    professionalName: [],
    fill: rePreference[data.gaokao.preference] || '专业',
  };

  gaokaoAddingScoreItems.forEach((item, index) => {
    if (item.key === data.gaokao.bonus.key) {
      userInfo.awarded.name = item.content;
    }
  });

  regionalData.forEach((r, i) => {
    regionalListData[r.key].forEach((item, index) => {
      if (
        data.interest.institute_locations &&
        data.interest.institute_locations.indexOf(item.key) > -1
      ) {
        userInfo.regionalName.push(item.value);
      }
    });
  });

  professionalData.forEach((r, i) => {
    professionalListData[r.key].forEach((item, index) => {
      if (
        data.interest.field_of_study_category_keys &&
        data.interest.field_of_study_category_keys.indexOf(item.key) > -1
      ) {
        userInfo.professionalName.push(item.value);
      }
    });
  });

  data.gaokao.independent_recruitment.forEach((r, i) => {
    if (r.institute_slug) {
      userInfo.recruit.push({
        kind: r.institute.native_name ? r.institute.native_name : r.institute.wikipedia.localized_name.cn,
        score: r.score,
        slug: r.institute_slug,
      });
    }
  });

  return userInfo;
};

